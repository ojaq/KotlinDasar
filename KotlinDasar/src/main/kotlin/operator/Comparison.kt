package operator

fun main () {
    // operator that compares 2 value and the output will be boolean
    // if the comparison of 2 value make sense output = true
    // if the comparison of 2 value doesn't make sense output = false

    val a = 3
    val b = 4

    println("a = $a")
    println("b = $b")

    // comparison operator
    println("a > b = ${a > b}")
    println("a < b = ${a < b}")
    println("a >= b = ${a >= b}")
    println("a >= b = ${a >= b}")
    println("a == b = ${a == b}")
    println("a != b = ${a != b}")
}